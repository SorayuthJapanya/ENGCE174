def fibonacci():
    a, b = 0, 1
    while True:
        yield a 
        a, b = b, a + b
fib = fibonacci()
print(next(fib))
print(next(fib))

    #คำสั่ง yield ใน Python เป็นฟีเจอร์ที่ใช้ในฟังก์ชันเพื่อสร้าง generator ซึ่งเป็นวิธีการสร้าง
    # สิ่งที่วนลูปได้ อย่างมีประสิทธิภาพ โดย yield ช่วยให้สามารถคืนค่าจากฟังก์ชันทีละค่า 
    # และยังคงสถานะการทำงานของฟังก์ชันไว้ ทำให้สามารถกลับมาทำงานต่อจากจุดเดิมได้เมื่อ
    # มีการเรียกครั้งต่อไป